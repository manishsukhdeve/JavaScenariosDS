package StringConcepts;

public class LengthMethod {

	public static void main(String[] args) {

		// public int length(); 
		// Return Number of Characters Present in the String.
		// length is the Variable applicable for Array Objects where as length() is the Method applicable for String Objects.
		
		String s = "Durga";
		System.out.println(s.length());	// length() method is use for String.
		
		int [] x = {10,20,60};
		System.out.println(x.length); // length variable is use for array concept.

	}

}
